    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (posicionEncontrada == -1) {
        JOptionPane.showMessageDialog(this, "Primero busque un producto");
        return;
    }

    int confirmacion = JOptionPane.showConfirmDialog(
            this,
            "¿Desea eliminar este producto?",
            "Confirmar eliminación",
            JOptionPane.YES_NO_OPTION
    );

    if (confirmacion == JOptionPane.YES_OPTION) {

        for (int i = posicionEncontrada; i < ProductoControllers.contador - 1; i++) {
            ProductoControllers.productos[i] = ProductoControllers.productos[i + 1];
        }

        ProductoControllers.productos[ProductoControllers.contador - 1] = null;
        ProductoControllers.contador--;

        JOptionPane.showMessageDialog(this, "Producto eliminado");

        jTextArea1.setText("");
        txtID.setText("");
        posicionEncontrada = -1;
    }
    }//GEN-LAST:event_btnEliminarActionPerformed


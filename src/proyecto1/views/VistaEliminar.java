    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
    String id = txtID.getText().trim();
    posicionEncontrada = -1;

    for (int i = 0; i < ProductoControllers.contador; i++) {

        if (ProductoControllers.productos[i].id.equalsIgnoreCase(id)) {

            ProductoModels p = ProductoControllers.productos[i];

            jTextArea1.setText(
                    "ID: " + p.id +
                    "\nNombre: " + p.nombre +
                    "\nCategoria: " + p.categoria +
                    "\nPrecio: " + p.precio +
                    "\nStock: " + p.stock
            );

            posicionEncontrada = i;
            return;
        }
    }

    jTextArea1.setText("Producto no encontrado");
    }//GEN-LAST:event_btnBuscarActionPerformed


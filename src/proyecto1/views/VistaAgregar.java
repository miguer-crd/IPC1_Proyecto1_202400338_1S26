    private void imprimirInventario() {
        System.out.println("============================== INVENTARIO =============================");
        for (int i = 0; i < ProductoControllers.contador; i++) {
            ProductoModels p = ProductoControllers.productos[i];
            System.out.println("ID: " + p.id + ", Nombre: " + p.nombre + ", Categoria: " + p.categoria + ", Precio: " + p.precio + ", Stock: " + p.stock);
        }
        System.out.println("=======================================================================");
    }
    

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
    String id = txtID.getText();
    String nombre = txtNombre.getText();
    String categoria = txtCatego.getText();

    double precio;
    int stock;

    try {
        precio = Double.parseDouble(txtPrecio.getText());
        stock = Integer.parseInt(txtStock.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Precio o cantidad inválida");
        return;
    }

    if (precio <= 0 || stock <= 0) {
        JOptionPane.showMessageDialog(this, "El precio y la cantidad deben ser positivos");
        return;
    }

    for (int i = 0; i < ProductoControllers.contador; i++) {
        if (ProductoControllers.productos[i].id.equals(id)) {
            JOptionPane.showMessageDialog(this, "El ID ya existe");
            return;
        }
    }

    ProductoModels nuevo = new ProductoModels(id, nombre, categoria, precio, stock);
    ProductoControllers.productos[ProductoControllers.contador] = nuevo;
    ProductoControllers.contador++;

    JOptionPane.showMessageDialog(this, "Producto agregado correctamente");

    // Limpiar los campos después de agregar
    txtID.setText("");
    txtNombre.setText("");
    txtCatego.setText("");
    txtPrecio.setText("");
    txtStock.setText("");
    imprimirInventario(); // Muestra el inventario en la consola
    }//GEN-LAST:event_btnAgregarActionPerformed

